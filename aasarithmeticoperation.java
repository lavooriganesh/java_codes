public class aasarithmeticoperation {
    public static void main(String[] args) {
        int a = 20;
        
        int sum = a + 5;
        int difference = a - 5;
        int product = a * 2;
        int quotient = a / 4;
  
        System.out.println("a + 5 = " + sum);
        System.out.println("a - 5 = " + difference);
        System.out.println("a * 2 = " + product);
        System.out.println("a / 4 = " + quotient);
    }
}
